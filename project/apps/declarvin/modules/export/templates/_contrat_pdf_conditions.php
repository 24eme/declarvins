        <h2>Article 2- Informatique et Liberté</h2>
        <p>Les informations recueillies font l'objet d'un traitement informatique destiné à l'Information collective statistique. Les destinataires des données sont les Interprofessions et les services compétents.</p>
        <p>Conformément à la loi &laquo;informatique et libertés&raquo; du 6 janvier 1978, vous bénéficiez d'un droit d'accès et de rectification aux informations qui vous concernent. Si vous souhaitez exercer ce droit et obtenir communication des informations vous concernant, veuillez vous adresser aux Interprofessions.</p>
