        <h2>Article 1 – Responsabilités</h2>
        
        <h3>Utilisateurs</h3>
        
        <p>&laquo; DeclarVins &raquo; est un outil proposé par les Interprofessions permettant de réaliser ces déclarations auprès des Interprofessions. Les destinataires des contenus de chaque déclaration sont précisés, et respectent les obligations et la légitimité de ces flux d'informations</p>
        
        <h4>Déclarant DeclarVins</h4>
        
        <p>Définition : le déclarant Declarvins est la personne identifiée sur declarvins comme responsable du compte.</p>
		<p>Chaque compte est accessible via un identifiant et mot de passe uniques choisis par le déclarant.</p>
		<p>Chaque déclarant est responsable de la gestion de son compte Déclarant et de sa mise à jour : nom, prénom, fonction, e-mail ; ainsi que de la gestion de l'identifiant et du mot de passe associés.</p>
		<p>Chaque compte peut donner accès à un ou plusieurs établissements.</p>
        
        <h4>Etablissement(s)</h4>
        
        <p>Définition : personnes morales du ressort de l'Interprofession. Elles peuvent être une entreprise de production ou de négoce. Ce DeclarVins est également ouvert aux courtiers en vin.</p>
		<p>Un établissement (à l'exception des courtiers) possède un ou plusieurs chais de stockage ou conditionnement. Il est identifié par un seul SIRET, un seul numéro d'Entrepositaire Agréé, qui correspond à un seul établissement devant réaliser la majorité des déclarations obligatoires spécifiques à la filière vin, notamment auprès des douanes.</p>
        
        <h4>Déclarations multiples</h4>
        
        <p>En cas de déclarations multiples (EDI, et/ou DTI, et/ou papier), les procédures techniques rendent les informations saisies (DTI) prioritaires sur les informations envoyées (EDI), elles-mêmes prioritaires sur les informations papier.</p>
        <p>Toutes déclarations postérieures à la première déclaration validées doit avoir le statut de déclaration rectificative et faire l'objet d'un accord des services compétents.</p>
        
        <div class="page_break">&nbsp;</div>
        
        <h3>Interprofessions</h3>
        <br />
        <br />
        <h4>Mise à disposition des outils déclaratifs et engagement de service</h4>
        
        <p>Les Interprofessions s'engagent à ce que &laquo; DeclarVins &raquo; donne les moyens aux déclarants d'effectuer leurs déclarations obligatoires auprès des Interprofessions (comparable à la liasse papier). L'information est transmise instantanément et en toute transparence au déclarant (visualisation définitive avant envoi).</p> 
        <p>Une fois les déclarations validées par les déclarants, elles sont reçues par l'interprofession qui fournit un numéro unique à chacune.</p>
        <p>Des mises à jour peuvent être faites sur le système pour répondre aux évolutions règlementaires. Les évolutions du système &laquo; DeclarVins &raquo; donneront lieu à une information faite par les Interprofessions dans des délais raisonnables.</p>
        <p>Les Interprofessions informent les déclarants concernés en cas de changement d'adhésion d'organismes représentatifs de produits aux Interprofessions, et d'interlocuteur de gestion des déclarants sur &laquo; DeclarVins &raquo;.</p>
        <p>Un protocole de sécurité, de sauvegarde et de résolution des problèmes en moins de 4h00 est établi avec le prestataire hébergeant la solution &laquo; DeclarVins &raquo;.</p>
        <p>Des alertes sont prévues en cas de disfonctionnement d'un transfert d'information dématérialisée, et informeront sur les procédures à mettre en place éventuellement par les déclarants (procédures de secours).</p>
        
        <h4>Procédures de secours </h4>
        
        <p>Dans le cas extrême où le problème informatique ne pourrait être résolu dans les délais impartis, une procédure de secours sera en place via l'utilisation des liasses papier toujours valides.</p>
        
